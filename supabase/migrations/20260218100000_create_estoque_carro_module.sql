CREATE TABLE "ID ESTOQUE CARRO" (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    data_inicio TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    data_fim TIMESTAMP WITH TIME ZONE,
    funcionario_id BIGINT NOT NULL REFERENCES "FUNCIONARIOS"(id)
);

CREATE TABLE "ESTOQUE CARRO SALDO INICIAL" (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    id_estoque_carro BIGINT NOT NULL REFERENCES "ID ESTOQUE CARRO"(id) ON DELETE CASCADE,
    timestamp TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    funcionario_nome TEXT,
    funcionario_id BIGINT REFERENCES "FUNCIONARIOS"(id),
    produto_id BIGINT NOT NULL REFERENCES "PRODUTOS"("ID"),
    codigo_produto BIGINT,
    barcode TEXT,
    produto TEXT,
    preco DECIMAL(10,2),
    saldo_inicial INTEGER DEFAULT 0
);

CREATE TABLE "ESTOQUE CARRO SALDO FINAL" (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    id_estoque_carro BIGINT NOT NULL REFERENCES "ID ESTOQUE CARRO"(id) ON DELETE CASCADE,
    timestamp TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    funcionario_nome TEXT,
    funcionario_id BIGINT REFERENCES "FUNCIONARIOS"(id),
    produto_id BIGINT NOT NULL REFERENCES "PRODUTOS"("ID"),
    codigo_produto BIGINT,
    barcode TEXT,
    produto TEXT,
    preco DECIMAL(10,2),
    saldo_final INTEGER DEFAULT 0
);

CREATE TABLE "ESTOQUE CARRO CONTAGEM" (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    id_estoque_carro BIGINT NOT NULL REFERENCES "ID ESTOQUE CARRO"(id) ON DELETE CASCADE,
    timestamp TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    funcionario_nome TEXT,
    funcionario_id BIGINT REFERENCES "FUNCIONARIOS"(id),
    produto_id BIGINT NOT NULL REFERENCES "PRODUTOS"("ID"),
    quantidade INTEGER DEFAULT 0
);

CREATE TABLE "ESTOQUE CARRO AJUSTES" (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    id_estoque_carro BIGINT NOT NULL REFERENCES "ID ESTOQUE CARRO"(id) ON DELETE CASCADE,
    timestamp TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    produto_id BIGINT NOT NULL REFERENCES "PRODUTOS"("ID"),
    diferenca_quantidade INTEGER DEFAULT 0,
    diferenca_valor DECIMAL(10,2) DEFAULT 0,
    ajuste_manual INTEGER DEFAULT 0,
    novo_saldo INTEGER DEFAULT 0
);

-- Log tables as requested (even if business logic primarily aggregates from other sources, these exist for potential direct logging)
