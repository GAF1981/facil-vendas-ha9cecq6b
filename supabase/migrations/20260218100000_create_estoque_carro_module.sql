-- Indexes for performance
CREATE INDEX idx_estoque_carro_func ON "ID ESTOQUE CARRO" (funcionario_id);
CREATE INDEX idx_ec_saldo_ini_sess ON "ESTOQUE CARRO SALDO INICIAL" (id_estoque_carro);
CREATE INDEX idx_ec_saldo_fin_sess ON "ESTOQUE CARRO SALDO FINAL" (id_estoque_carro);
