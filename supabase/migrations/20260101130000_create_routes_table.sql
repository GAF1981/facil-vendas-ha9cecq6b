CREATE TABLE IF NOT EXISTS "CRIAR_NOVA_ROTA" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_rota TEXT NOT NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS criar_nova_rota_nome_rota_idx ON "CRIAR_NOVA_ROTA" (nome_rota);

INSERT INTO "CRIAR_NOVA_ROTA" (nome_rota)
SELECT DISTINCT "GRUPO ROTA"
FROM "CLIENTES"
WHERE "GRUPO ROTA" IS NOT NULL AND "GRUPO ROTA" <> ''
ON CONFLICT (nome_rota) DO NOTHING;

CREATE OR REPLACE FUNCTION get_unique_client_routes()
RETURNS TABLE (rota text)
LANGUAGE plpgsql
AS $$
BEGIN
  RETURN QUERY
  SELECT nome_rota as rota
  FROM "CRIAR_NOVA_ROTA"
  ORDER BY nome_rota;
END;
$$;
